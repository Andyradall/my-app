<strong>Process Awareness Post-Hyper:</strong> I notice that I am much more process aware now than before Hyper, I am aware of where in the double diamond process we are, and allow myself more freedom and am less strict on myself in the divergent phases - this makes it easier for me to be more creative!<br>
