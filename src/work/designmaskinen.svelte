<div id="content_d94b45d7e" class="column-content" data-module="text">
    <div class="is-content"><ul>
<li>
<strong>Challenging my Bias:</strong> Before going into this, I had this perception of consultants as conformist individuals, always in suits and perhaps a bit sharp elbowed. However, the team at Netlife completely defied this image – they were actually super cool and down to earth! At the same time there were a lot of skilled and very collaborative people gathered in one environment and that was amazing. I learned a lot from my internship!</li>
<li>
<strong>Social Workplace:</strong> There were a lot of social activities to join, we went grilling and playing in the park, we went to a design award, we went swimming and doing saunas. I even joined their running team for a race - it was a lot of fun there!</li>
<li>
<strong>Collaboration:&nbsp;</strong>Working in a cross functional team with developers, having the developers in the same room as me. It made it quick to ideate and shoot ideas back and forth between designers and developers.&nbsp;</li>
<li>
<strong>Prioritizing Accessibility:</strong> I learned about designing for the highest accessibility and WCAG standards. I learned to design interfaces that can be used on screen readers and navigated with a keyboard.</li>
