<div id="content_qi91n1mao" class="column-content" data-module="paragraph">
<div class="is-content"><div data-content-for="xl"><div id="first" class="section"><h2><span class="font_c950ltmye">Doing Research in Covid Lockdown</span></h2></div></div></div>
</div>

</div>
</div></div>
<div id="row_5bfa013a0" class="row"><div id="column_682bad8bb" class="column" data-xl-width="8">
<div class="content-wrapper">
    
<div id="content_2a04fnkkm" class="column-content" data-module="text">
<div class="is-content"><p>The objective was clear: enhance profile content on KS by increaing engagement. At the same time, there were challenges, like layoffs and the pandemic restrictions. The question we grappled with was, "How do we get user insights under these conditions?"<br><br></p></div>
</div>

<div id="content_nuucuze5z" class="column-content" data-module="text">
<div class="is-content"><h3>Proactive Measures:</h3></div>
</div>

<div id="content_0g68gsgvt" class="column-content" data-module="text">
<div class="is-content"><p>To get insight into user behavior, I took initiative to integrate Hotjar on the portal. Its recordings and heatmaps vividly depicted areas where the user journey stuttered. Furthermore, I set up a feedback module, so we could zoom in on specific user pain points.</p></div>
</div>

<div id="content_qfj0o5aol" class="column-content" data-module="text">
<div class="is-content"><h3>Remote Insights:</h3></div>
</div>

<div id="content_zvhhcjzjm" class="column-content" data-module="text">
<div class="is-content"><p>Traditional face-to-face interviews were off the table. Instead, I dispatched surveys for insights and turned to remote platforms like Teston.io and Usertesting.com. The remote validation of our designs turned out to be highly effective.</p></div>
</div>

<div id="content_subdkpuot" class="column-content" data-module="text">
<div class="is-content"><h3>Fostering User Engagement:</h3></div>
</div>

<div id="content_n7drgrsoq" class="column-content" data-module="text">
<div class="is-content"><p>With an eye on further amplifying user engagement, I studied Nir Eyal's Hook model. Then I explored ways to incorporate this framework into our platform.</p></div>
</div>

</div>
</div></div>
<div id="row_gcle0bz5v" class="row"><div id="column_0jqn61k5o" class="column" data-xl-width="10">
<div class="content-wrapper">
    
<div id="content_xna4docq7" class="column-content" data-module="image">
